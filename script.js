const kleur = ['geel', 'blauw', 'rood', 'oranje'];

//while
let i = 0;
while (i < kleur.length) {
    console.log('This is while iteration number [i]');
    i++;
}

//for  
for (let i = 0; i < kleur.length; i++) {
    console.log('This is while iteration number [i]');
}

//forEach
kleur.forEach((benoemdeKleur) => console.log(benoemdeKleur));

